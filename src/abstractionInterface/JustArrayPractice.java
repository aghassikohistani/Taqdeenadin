public class JustArrayPractice {

	public static void main(String[] args) {
		
		
		String [] students = new String [3];
		
		
		
		students [0] = "John";
		students [1] = "Smith";
		students [2] = "Scot";
		
		for(int i =0; i<3; i++) {
			System.out.println(students[i]);
		}
		
	}
}
